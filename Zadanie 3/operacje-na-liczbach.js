//Deklaracja zmiennych a o wartości 10 oraz b o wartości 3.
let a = 10;
let b= 3;

//Wypisanie na ekran wartości liczb a oraz b, jedna po drugiej, oddzielone przecinkiem.
alert(a + ',' + b);

//Wypisanie na ekran iloczynu liczb a i b
alert(a*b);

//Wypisanie na ekran ilorazu liczb a i b.
alert(a/b);

//Wypisanie na ekran reszty z dzielenia a przez 2.
alert(a%2);

//Wypisanie na ekran ilorazu liczb a i b, wynik zaokrąglony do liczby całkowitej
alert(parseInt(a/b));

//Wypisanie na ekran pierwiastka z a.
alert(Math.sqrt(a));

//Wypisanie na ekran piątej potęgi liczby b.
alert(Math.pow(b, 5));

//Wypisanie na ekran wartości absolutnej z -b
alert(Math.abs(-b));